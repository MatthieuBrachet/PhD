%%% Function f2 in Fornberg-Martel p.1176

function res = fun_f2( x,y,z )

    res = 0.75*exp(-(9*x-2).^2/4-(9*y-2).^2/4-(9*z-2).^2/4)+...
        0.75*exp(-(9*x+1).^2/49-(9*y+1)/10-(9*z+1)/10)+...
        0.5*exp(-(9*x-7).^2/4-(9*y-3).^2/4-(9*z-5).^2/4) ...
        -0.2*exp(-(9*x-4).^2-(9*y-7).^2-(9*z-5).^2);
    
end